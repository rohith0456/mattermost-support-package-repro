func mattermostService(name, image string, port int, siteURL string, p *models.ReproPlan) string {
	// Use the correct DB service name in depends_on
	dbServiceName := "postgres"
	if p.Services.Database.Type == "mysql" {
		dbServiceName = "mysql"
	}
	var deps strings.Builder
	deps.WriteString(fmt.Sprintf("    depends_on:\n      %s:\n        condition: service_healthy\n", dbServiceName))
	if p.Services.Search.Enabled {
		deps.WriteString("      opensearch:\n        condition: service_healthy\n")
	}
	if p.Services.FileStorage.UseMinIO {
		deps.WriteString("      minio:\n        condition: service_healthy\n")
	}
	if p.Services.Auth.KeycloakEnabled {
		deps.WriteString("      keycloak:\n        condition: service_healthy\n")
	}

	var extra strings.Builder
	// Shared file storage (required for HA / multi-node)
	if p.Services.FileStorage.UseMinIO || (p.Topology == "multi-node" && p.NodeCount > 1) {
		extra.WriteString(fmt.Sprintf(`      MM_FILESETTINGS_DRIVERNAME: amazons3
      MM_FILESETTINGS_AMAZONS3ACCESSKEYID: ${MINIO_ROOT_USER:-minioadmin}
      MM_FILESETTINGS_AMAZONS3SECRETACCESSKEY: ${MINIO_ROOT_PASSWORD:-minio_local_repro_only}
      MM_FILESETTINGS_AMAZONS3BUCKET: mattermost
      MM_FILESETTINGS_AMAZONS3ENDPOINT: minio:9000
      MM_FILESETTINGS_AMAZONS3SSL: "false"
      MM_FILESETTINGS_AMAZONS3PATHSTYLE: "true"
`))
	}
	// Cluster (HA)
	if p.Topology == "multi-node" && p.NodeCount > 1 {
		extra.WriteString(`      MM_CLUSTERSETTINGS_ENABLE: "true"
      MM_CLUSTERSETTINGS_CLUSTERNAME: repro-cluster
`)
	}
	// OpenSearch (requires Enterprise license — pre-configured, activates on license upload + restart)
	if p.Services.Search.Enabled {
		extra.WriteString(`      MM_ELASTICSEARCHSETTINGS_CONNECTIONURL: http://opensearch:9200
      MM_ELASTICSEARCHSETTINGS_ENABLEINDEXING: "true"
      MM_ELASTICSEARCHSETTINGS_ENABLESEARCHING: "true"
      MM_ELASTICSEARCHSETTINGS_ENABLEAUTOCOMPLETE: "true"
      MM_ELASTICSEARCHSETTINGS_SKIPTLSVERIFICATION: "true"
      MM_ELASTICSEARCHSETTINGS_USERNAME: ""
      MM_ELASTICSEARCHSETTINGS_PASSWORD: ""
`)
	}
	// LDAP
	if p.Services.Auth.LDAPEnabled {
		extra.WriteString(`      MM_LDAPSETTINGS_ENABLE: "true"
      MM_LDAPSETTINGS_LDAPSERVER: openldap
      MM_LDAPSETTINGS_LDAPPORT: "389"
      MM_LDAPSETTINGS_BASEDN: dc=repro,dc=local
      MM_LDAPSETTINGS_BINDUSERNAME: cn=readonly,dc=repro,dc=local
      MM_LDAPSETTINGS_BINDPASSWORD: ${LDAP_BIND_PASSWORD:-readonly_local_repro_only}
      MM_LDAPSETTINGS_USERFILTER: "(objectClass=inetOrgPerson)"
      MM_LDAPSETTINGS_USERNAMEATTRIBUTE: uid
      MM_LDAPSETTINGS_FIRSTNAMEATTRIBUTE: givenName
      MM_LDAPSETTINGS_LASTNAMEATTRIBUTE: sn
      MM_LDAPSETTINGS_EMAILATTRIBUTE: mail
      MM_LDAPSETTINGS_IDATTRIBUTE: uid
      MM_LDAPSETTINGS_LOGINIDATTRIBUTE: uid
      MM_LDAPSETTINGS_GROUPFILTER: "(objectClass=groupOfNames)"
      MM_LDAPSETTINGS_GROUPDISPLAYNAMEATTRIBUTE: cn
      MM_LDAPSETTINGS_GROUPMEMBERATTRIBUTE: member
      MM_LDAPSETTINGS_SYNCINTERVALMINUTES: "60"
`)
	}
	// OIDC via Keycloak (no license required — uses MM_GITLABSETTINGS_*)
	if p.Services.Auth.KeycloakEnabled {
		extra.WriteString(`      MM_GITLABSETTINGS_ENABLE: "true"
      MM_GITLABSETTINGS_ID: ${OIDC_CLIENT_ID:-mattermost-client}
      MM_GITLABSETTINGS_SECRET: ${OIDC_CLIENT_SECRET:-mattermost-secret-local-repro-only}
      MM_GITLABSETTINGS_AUTHENDPOINT: http://localhost:8080/realms/repro/protocol/openid-connect/auth
      MM_GITLABSETTINGS_TOKENENDPOINT: http://keycloak:8080/realms/repro/protocol/openid-connect/token
      MM_GITLABSETTINGS_USERAPIENDPOINT: http://keycloak:8080/realms/repro/protocol/openid-connect/userinfo
`)
	}
	// SAML via Keycloak (requires Enterprise license — pre-configured, activates on license upload)
	// MM_SAMLSETTINGS_IDPMETADATAURL causes Mattermost to auto-fetch the cert from Keycloak.
	// No manual cert upload in System Console is needed.
	if p.Services.Auth.SAMLEnabled {
		extra.WriteString(`      MM_SAMLSETTINGS_ENABLE: "true"
      MM_SAMLSETTINGS_VERIFY: "false"
      MM_SAMLSETTINGS_ENCRYPT: "false"
      MM_SAMLSETTINGS_IDPMETADATAURL: http://keycloak:8080/realms/repro/protocol/saml/descriptor
      MM_SAMLSETTINGS_IDPURL: http://keycloak:8080/realms/repro/protocol/saml
      MM_SAMLSETTINGS_IDPISSUERURL: http://keycloak:8080/realms/repro
      MM_SAMLSETTINGS_SERVICEPROVIDERENTIFIER: http://localhost:8065
      MM_SAMLSETTINGS_ASSERTIONCONSUMERSERVICEURL: http://localhost:8065/login/sso/saml
      MM_SAMLSETTINGS_EMAILATTRIBUTE: email
      MM_SAMLSETTINGS_USERNAMEATTRIBUTE: preferred_username
      MM_SAMLSETTINGS_FIRSTNAMEATTRIBUTE: given_name
      MM_SAMLSETTINGS_LASTNAMEATTRIBUTE: family_name
`)
	}
	// License pre-loaded (when --license flag was provided at init time)
	if p.LicenseProvided {
		extra.WriteString(`      MM_SERVICESETTINGS_LICENSEFILELOCATION: /mattermost/licenses/mattermost.mattermost-license
`)
	}

	// Extra bind-mount volumes (e.g. license file pre-load)
	var extraVolumes strings.Builder
	if p.LicenseProvided {
		extraVolumes.WriteString("      - ./licenses:/mattermost/licenses:ro\n")
	}

	listenPort := fmt.Sprintf("%d", port)

	// Build correct DB driver and datasource defaults based on the plan's DB type
	dbDriver := "postgres"
	dbDatasource := "postgres://mmuser:mmuser_password@postgres:5432/mattermost?sslmode=disable"
	if p.Services.Database.Type == "mysql" {
		dbDriver = "mysql"
		dbDatasource = "mmuser:mmuser_password@tcp(mysql:3306)/mattermost?charset=utf8mb4,utf8&readTimeout=30s&writeTimeout=30s"
	}

	return fmt.Sprintf(`
  %s:
    image: %s
    platform: linux/amd64
    restart: unless-stopped
    environment:
      MM_SQLSETTINGS_DRIVERNAME: ${MM_DB_DRIVER:-%s}
      MM_SQLSETTINGS_DATASOURCE: ${MM_DATASOURCE:-%s}
      MM_SERVICESETTINGS_SITEURL: %s
      MM_SERVICESETTINGS_LISTENADDRESS: ":%s"
      MM_EMAILSETTINGS_SMTPSERVER: mailpit
      MM_EMAILSETTINGS_SMTPPORT: "${MAILPIT_SMTP_PORT:-1025}"
      MM_EMAILSETTINGS_ENABLESMTPAUTH: "false"
      MM_LOGSETTINGS_CONSOLELEVEL: INFO
      MM_LOGSETTINGS_FILELEVEL: DEBUG
%s    ports:
      - "${MM_PORT:-%d}:%s"
    volumes:
      - %s_logs:/mattermost/logs
      - %s_config:/mattermost/config
      - %s_plugins:/mattermost/plugins
      - %s_client_plugins:/mattermost/client/plugins
%s    networks:
      - mm-repro
%s    healthcheck:
      disable: true

`, name, image, dbDriver, dbDatasource, siteURL, listenPort, extra.String(), port, listenPort, name, name, name, name, extraVolumes.String(), deps.String())
}

func nginxService(nodeCount int) string {
	var deps strings.Builder
	for i := 1; i <= nodeCount; i++ {
		deps.WriteString(fmt.Sprintf("      - mattermost-%d\n", i))
	}
	return fmt.Sprintf(`
  nginx:
    image: nginx:alpine
    restart: unless-stopped
    command: /bin/sh -c "rm -f /etc/nginx/conf.d/default.conf && nginx -g 'daemon off;'"
    ports:
      - "80:80"
    volumes:
      - ./nginx/nginx.conf:/etc/nginx/conf.d/mattermost.conf:ro
    depends_on:
%s    networks:
      - mm-repro

`, deps.String())
}

