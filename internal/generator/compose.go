// generateCompose generates the docker-compose.yml for the repro project.
func (g *Generator) generateCompose() (string, error) {
	var sb strings.Builder
	p := g.plan

	sb.WriteString("# Generated by mm-repro " + version.Short() + "\n")
	sb.WriteString("# DO NOT commit this file with real secrets — see .env for configuration\n")
	sb.WriteString("# This environment is for LOCAL DEBUGGING ONLY\n\n")
	sb.WriteString("services:\n")

	// Database
	if p.Services.Database.Enabled {
		if p.Services.Database.Type == "mysql" {
			sb.WriteString(mysqlService(p.Services.Database.ExposedPort))
		} else {
			sb.WriteString(postgresService(p.Services.Database.ExposedPort))
		}
	}

	// Search
	if p.Services.Search.Enabled {
		sb.WriteString(opensearchService(p.Services.Search.ExposedPort))
	}

	// LDAP
	if p.Services.Auth.LDAPEnabled {
		sb.WriteString(ldapService())
	}

	// Keycloak
	if p.Services.Auth.KeycloakEnabled {
		sb.WriteString(keycloakService())
	}

	// MinIO
	if p.Services.FileStorage.UseMinIO {
		sb.WriteString(minioService(p.Services.FileStorage.ExposedPort))
	}

	// MailHog (always included)
	sb.WriteString(mailhogService(p.Services.Email.ExposedPort, p.Services.Email.UIPort))

	// Mattermost (single or replicas)
	image := fmt.Sprintf("%s:%s", p.MattermostImage, p.Services.Mattermost.Tag)
	siteURL := "${MM_SITE_URL:-http://localhost}"
	if p.Topology != "multi-node" {
		siteURL = "${MM_SITE_URL:-http://localhost:8065}"
	}
	if p.Topology == "multi-node" && p.NodeCount > 1 {
		for i := 0; i < p.NodeCount; i++ {
			name := fmt.Sprintf("mattermost-%d", i+1)
			port := 8065 + i
			sb.WriteString(mattermostService(name, image, port, siteURL, p))
		}
		sb.WriteString(nginxService(p.NodeCount))
	} else {
		sb.WriteString(mattermostService("mattermost", image, 8065, siteURL, p))
	}

	// RTCD
	if p.Services.Calls.UseRTCD {
		sb.WriteString(rtcdService(p.Services.Calls.ExposedPort))
	}

	// Prometheus
	if p.Services.Observability.PrometheusEnabled {
		sb.WriteString(prometheusService(p.Services.Observability.PrometheusPort))
	}

	// Grafana
	if p.Services.Observability.GrafanaEnabled {
		sb.WriteString(grafanaService(p.Services.Observability.GrafanaPort))
	}

	// ngrok tunnel (mobile / remote access)
	if p.Services.Tunnel.NgrokEnabled {
		var tunnelTarget string
		if p.Topology == "multi-node" && p.NodeCount > 1 {
			tunnelTarget = "nginx:80"
		} else {
			tunnelTarget = fmt.Sprintf("mattermost:%d", p.Services.Mattermost.ExposedPort)
		}
		sb.WriteString(ngrokService(tunnelTarget, p.Services.Tunnel.NgrokAPIPort))
	}

	// Networks and volumes
	sb.WriteString(networksAndVolumes(p))

	return g.writeFile("docker-compose.yml", sb.String())
}

func postgresService(port int) string {
	return fmt.Sprintf(`
  postgres:
    image: postgres:15-alpine
    restart: unless-stopped
    environment:
      POSTGRES_USER: ${MM_DB_USER:-mmuser}
      POSTGRES_PASSWORD: ${MM_DB_PASSWORD:-mmuser_password}
      POSTGRES_DB: ${MM_DB_NAME:-mattermost}
    ports:
      - "%d:5432"
    volumes:
      - postgres_data:/var/lib/postgresql/data
    networks:
      - mm-repro
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U $${POSTGRES_USER:-mmuser}"]
      interval: 10s
      timeout: 5s
      retries: 5

`, port)
}

